package AtividadesAula5;

public class Onibus implements InterfaceVeiculo{
	
	public void ligar() {
		System.out.println("O onibus esta ligado.");
	}
	
	public void desligar() {
		System.out.println("O onibus esta desligado.");
	}
	
	public void manobrar() {
		System.out.println("O onibus esta manobrando.");
	}
	
	public void engatar() {
		System.out.println("O onibus esta engatado.");
	}
	
	public void acelerar() {
		System.out.println("O onibus esta acelerando.");
	}
	
	public void frear() {
		System.out.println("O onibus esta freando.");
	}
	
}
