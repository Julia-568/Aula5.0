package AtividadesAula5;

public class Ferrari implements InterfaceVeiculo {
	
	public void ligar() {
		System.out.println("A ferrari esta ligada.");
	}
	
	public void desligar() {
		System.out.println("A ferrari esta desligada.");
	}
	
	public void manobrar() {
		System.out.println("A ferrari esta manobrando.");
	}
	
	public void engatar() {
		System.out.println("A ferrari esta engatada.");
	}
	
	public void acelerar() {
		System.out.println("A ferrari esta acelerando.");
	}
	
	public void frear() {
		System.out.println("A ferrari esta freando.");
	}
	
	

}
